-- FUNCTION CODE

CREATE OR REPLACE FUNCTION RETRIVE(ANO LOAN.ACC_NO%TYPE)
RETURN NUMBER
IS
    
    BAL LOAN.AMOUNT%TYPE;

BEGIN

    SELECT AMOUNT INTO BAL FROM LOAN WHERE ACC_NO = ANO; 

    RETURN BAL;

END;


-- EXECUTION CODE


DECLARE 

    ANO LOAN.ACC_NO%TYPE;
    ANS LOAN.AMOUNT%TYPE;
BEGIN

    DBMS_OUTPUT.PUT_LINE('Enter account number');
    ANO := :ANO;

    ANS := RETRIVE(ANO);

    DBMS_OUTPUT.PUT_LINE('Balance is: ' || ANS);

END;

